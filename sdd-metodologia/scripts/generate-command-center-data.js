#!/usr/bin/env node
/**
 * generate-command-center-data.js — Generate shared/data.js for SDD Command Center
 *
 * Usage: node scripts/generate-command-center-data.js <project-path>
 * Output: <project-path>/.specify/shared/data.js
 */

const fs = require('fs');
const path = require('path');

const projectPath = process.argv[2] || '.';
const specsDir = path.join(projectPath, 'specs');
const specifyDir = path.join(projectPath, '.specify');
const outputDir = path.join(specifyDir, 'shared');
const outputFile = path.join(outputDir, 'data.js');

function readJSON(fp) {
  try { return JSON.parse(fs.readFileSync(fp, 'utf8')); } catch { return null; }
}

function readFile(fp) {
  try { return fs.readFileSync(fp, 'utf8'); } catch { return null; }
}

// Scan features
const features = [];
if (fs.existsSync(specsDir)) {
  fs.readdirSync(specsDir).filter(d => {
    const full = path.join(specsDir, d);
    return fs.statSync(full).isDirectory() && !d.startsWith('.');
  }).forEach(dir => {
    const featureDir = path.join(specsDir, dir);
    const hasSpec = fs.existsSync(path.join(featureDir, 'spec.md'));
    const hasPlan = fs.existsSync(path.join(featureDir, 'plan.md'));
    const hasTasks = fs.existsSync(path.join(featureDir, 'tasks.md'));
    const hasTests = fs.existsSync(path.join(featureDir, 'tests'));

    // Count tasks
    let totalTasks = 0, completedTasks = 0;
    const tasksContent = readFile(path.join(featureDir, 'tasks.md'));
    if (tasksContent) {
      const allTasks = tasksContent.match(/- \[.\]/g) || [];
      const done = tasksContent.match(/- \[x\]/g) || [];
      totalTasks = allTasks.length;
      completedTasks = done.length;
    }

    features.push({
      id: dir,
      name: dir.replace(/^\d+-/, '').replace(/-/g, ' '),
      spec: hasSpec,
      plan: hasPlan,
      tasks: hasTasks,
      tests: hasTests,
      totalTasks,
      completedTasks,
      progress: totalTasks > 0 ? Math.round((completedTasks / totalTasks) * 100) : 0,
      status: completedTasks === totalTasks && totalTasks > 0 ? 'complete' : totalTasks > 0 ? 'in_progress' : 'pending'
    });
  });
}

// Constitution check
const hasConstitution = fs.existsSync(path.join(projectPath, 'CONSTITUTION.md'));
const hasPremise = fs.existsSync(path.join(projectPath, 'PREMISE.md'));

// Score history
const scoreHistory = readJSON(path.join(specifyDir, 'score-history.json')) || [];

// Sentinel state
const sentinel = readJSON(path.join(specifyDir, 'sentinel-state.json')) || {};

// Calculate overall health
const avgProgress = features.length > 0
  ? Math.round(features.reduce((sum, f) => sum + f.progress, 0) / features.length)
  : 0;

// Map to Command Center expected structure
const data = {
  generatedAt: new Date().toISOString(),
  project: { name: path.basename(path.resolve(projectPath)) },
  premise: { name: path.basename(path.resolve(projectPath)) },
  insights: { healthScore: avgProgress },
  features: features.map(f => ({
    ...f,
    phase: f.status,
    requirements: [],
    stories: [],
  })),
  constitution: hasConstitution ? { principles: [] } : null,
  governance: { principles: hasConstitution ? { length: 21 } : { length: 0 } },
  quality: { passRate: features.length > 0 ? Math.round(features.filter(f => f.tests).length / features.length * 100) : 0 },
  workspace: { files: { length: '130+' } },
  sentinel,
  scoreHistory,
  sessionLog: [],
  smartNav: features.some(f => f.status !== 'complete') ? {
    message: `${features.filter(f => f.status !== 'complete').length} feature(s) in progress`,
    command: '/sdd:status',
    action: 'Check pipeline status'
  } : null,
  summary: {
    totalFeatures: features.length,
    completeFeatures: features.filter(f => f.status === 'complete').length,
    totalTasks: features.reduce((s, f) => s + f.totalTasks, 0),
    completedTasks: features.reduce((s, f) => s + f.completedTasks, 0),
  }
};

// Write data.js
fs.mkdirSync(outputDir, { recursive: true });
fs.writeFileSync(outputFile, `/* Generated by SDD Command Center — ${data.generated} */\nwindow.DASHBOARD_DATA = ${JSON.stringify(data, null, 2)};\n`);

console.log(`Command Center data: ${outputFile}`);
console.log(`  Features: ${data.summary.totalFeatures} (${data.summary.completeFeatures} complete)`);
console.log(`  Tasks: ${data.summary.completedTasks}/${data.summary.totalTasks}`);
console.log(`  Health: ${data.insights.healthScore}%`);
